// tudo que colocamos em uma variavel é um objeto!
// com isso podemos acessar suas propriedades, funções ou metodos

void main() {
  //? O var identifica o valor que esta sendo atribuido a ele e se molda a isso(ele se tipa automatico)
  //? Exemplo, atribui uma string, então agora ele sempre será uma string
  var nomeCompleto = "Vitor";

  //Object é algo global (ele não se tipa)
  Object nome = "vitor";

  //diferente do var ele pode ter mais de um valor ou seja, agora ela pode ser string, mais depois pode ser int
  dynamic nomes = 'Vitor';
}
